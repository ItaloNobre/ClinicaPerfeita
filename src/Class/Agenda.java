package Class;


public class Agenda {
	
	private int hora;
	private int dia;
	private int mes;
	Cliente clienteAgendado;
	
	
	public int getHora() {
		return hora;
	}
	public void setHora(int hora) {
		this.hora = hora;
	}
	public int getDia() {
		return dia;
	}
	public void setDia(int dia) {
		this.dia = dia;
	}
	public int getMes() {
		return mes;
	}
	public void setMes(int mes) {
		this.mes = mes;
	}
	public Cliente getClienteAgendado() {
		return clienteAgendado;
	}
	public void setClienteAgendado(Cliente clienteAgendado) {
		this.clienteAgendado = clienteAgendado;
	}

	

}
